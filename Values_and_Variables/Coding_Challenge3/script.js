//Data 1
const dolphinAverageScore = (96 + 108 + 89) / 3; // 97.66666666...
const koalaAverageScore = (88 + 91 + 110) / 3; // 96.33333333...
console.log(`The Dolphins average score was ${dolphinAverageScore} points and the Koalas average score was ${koalaAverageScore} points!`)

if (dolphinAverageScore >= 100 || koalaAverageScore >= 100) {
    if (dolphinAverageScore > koalaAverageScore) {
        console.log(`The Dolphins have won by ${dolphinAverageScore - koalaAverageScore} points!`)
    } else if (koalaAverageScore > dolphinAverageScore) {
        console.log(`The Koalas have won by ${koalaAverageScore - dolphinAverageScore} points!`)
    } else {
        console.log("It was a hard fought tie.")
    }
} else {
    console.log("They both aren't worthy of a trophy.")
}

